let dataList = [];
const cardsSection = document.querySelector('.cards');
if (cardsSection) {
  if (cardsSection.id === 'logo') {
    dataList = logo;
  }
  else if (cardsSection.id === 'typography') {
    dataList = typography;
  }
  else if (cardsSection.id === 'contest') {
    dataList = contest;
  }
  else if (cardsSection.id === 'project') {
    dataList = project;
  }
  else if (cardsSection.id === 'graphic') {
    dataList = graphic;
  }
  else if (cardsSection.id === 'video') {
    dataList = video;
  }
  // 必要なら他のidも追加
}

// カード生成
const container = document.getElementById("cardsContainer");
if (container && dataList.length > 0) {
  dataList.forEach(item => {
    // 日本語が含まれているか判定（ひらがな・カタカナ・漢字）
    const isJapanese = /[\u3040-\u30FF\u4E00-\u9FFF]/.test(item.title);
    const metaClass = isJapanese ? 'card-meta font-Acumin-default font-zen-kaku-gothic' : 'card-meta font-Acumin-default';

    const card = document.createElement("div");
    card.className = "card";
    card.innerHTML = `
      <div class="card">
        <a href="${item.link}" target="_blank" style="text-decoration: none; color: #000;">
          <div class="card-image">
            <img src="${item.image}" alt="${item.title}">
          </div>
          <div class="card-divider"></div>
          <div class="${metaClass}">
            <p class="label font-Acumin-default">Title</p>
            <p class="title">${item.title}</p>
          </div>
          <p class="card-date font-Acumin-default">${item.date}</p>
        </a>
      </div>
    `;
    container.appendChild(card);
  });

    // JA or EN
  const titles = container.querySelectorAll('.card-meta .title');
  titles.forEach(title => {
    const len = title.textContent.length;
    // JA
    const isJapanese = /[\u3040-\u30FF\u4E00-\u9FFF]/.test(title.textContent);
    if (isJapanese) {
      if (len > 30) {
        title.style.letterSpacing = "0.2px";
      } else if (len > 15) {
        title.style.letterSpacing = "0.6px";
      } else {
        title.style.letterSpacing = "1.2px";
      }
    } else {
      if (len > 30) {
        title.style.letterSpacing = "0.8px";
      } else if (len > 15) {
        title.style.letterSpacing = "1.5px";
      } else {
        title.style.letterSpacing = "2.87px";
      }
    }
  });
}
